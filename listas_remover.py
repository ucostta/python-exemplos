alunos=['Viktor', 'Guizin', 'Carlos', 'Nobru', 'Marcos', 'Viktor']
print(f"Lista de alunos: {alunos}")
# Método remove()
alunos.remove('Viktor')
print(f"Lista de alunos: {alunos}")
# Método pop()
alunos.pop(2)
print(f"Lista de alunos: {alunos}")
# Função del
del alunos[3]
print(f"Lista de alunos: {alunos}")